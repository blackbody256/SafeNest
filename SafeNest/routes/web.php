Route::get('/redirect-by-role', function () {
    $role = auth()->user()->role;

    return match($role) {
        'admin' => redirect('/admin'),
        'customer' => redirect('/customer'),
        'underwriter' => redirect('/underwriter'),
        default => abort(403),
    };
})->middleware('auth');

